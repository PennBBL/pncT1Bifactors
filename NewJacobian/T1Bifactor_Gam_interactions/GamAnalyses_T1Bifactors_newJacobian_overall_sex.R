##########################################
#### GAM MODELS FOR T1 BIFACTOR STUDY ####
##########################################

#Load data
data.JAC <- readRDS("/data/jux/BBL/projects/pncT1AcrossDisorder/subjectData/n1394_T1_subjData.rds")

#Load library
library(mgcv)

#Get JAC variable names
jacComponents <- names(data.JAC)[grep("newJacobian_Nmf18",names(data.JAC))]

#Run gam models
JacModels <- lapply(jacComponents, function(x) {
  gam(substitute(i ~ s(age) + sex + averageManualRating + mood_4factorv2 + psychosis_4factorv2 + externalizing_4factorv2 + phobias_4factorv2 + overall_psychopathology_4factorv2 + overall_psychopathology_4factorv2*sex , list(i = as.name(x))), method="REML", data = data.JAC)
})

#Look at model summaries
models <- lapply(JacModels, summary)

#############################
#### INTERACTION RESULTS ####
#############################

#Pull p-values
p_sex <- sapply(JacModels, function(v) summary(v)$p.table[9,4])

#Convert to data frame
p_sex <- as.data.frame(p_sex)

#Print original p-values to three decimal places
p_sex_round <- round(p_sex,3)

#FDR correct p-values
p_sex_fdr <- p.adjust(p_sex[,1],method="fdr")

#Convert to data frame
p_sex_fdr <- as.data.frame(p_sex_fdr)

#To print fdr-corrected p-values to three decimal places
p_sex_fdr_round <- round(p_sex_fdr,3)

#List the JAC components that survive FDR correction
Jac_sex_fdr <- row.names(p_sex_fdr)[p_sex_fdr<0.05]

#Name of the JAC components that survive FDR correction
Jac_sex_fdr_names <- jacComponents[as.numeric(Jac_sex_fdr)]

#To check direction of coefficient estimates
sex_coeff <- models[as.numeric(Jac_sex_fdr)]
